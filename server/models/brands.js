const mongoose = require("mongoose");

const brandsSchema = new mongoose.Schema(
  {
    cName: {
      type: String,
      required: true,
    },
    cDescription: {
      type: String,
      required: true,
    },
    cImage: {
      type: String,
    },
    cStatus: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const brandsModel = mongoose.model("brands", brandsSchema);
module.exports = brandsModel;
